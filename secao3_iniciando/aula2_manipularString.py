# podemos usar vírgula para exibir no mesmo print vários valores
# o separador por padrão é um espaço vazio mas podemos modificar esse padrão usando: sep=''
# podemos definir um padrão para o final desse print, usando: end=''
print('\nexemplo sem alterações:')
print(12, 34)
print('\nexemplo com alterações:')
print(12, 34, sep='-', end=' :)')